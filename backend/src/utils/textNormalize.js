export function normalizeVietnameseText(value = '') {
  let text = String(value || '');
  for (const [bad, good] of MOJIBAKE_MAP.entries()) {
    text = text.split(bad).join(good);
  }
  return text.replace(/\s+/g, ' ').trim();
}

export function normalizeTextFields(target = {}, fields = []) {
  const next = { ...target };
  for (const field of fields) {
    if (typeof next[field] === 'string') next[field] = normalizeVietnameseText(next[field]);
  }
  return next;
}
